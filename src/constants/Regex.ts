/**
 * Global Regex
 */
export const RegExp = {
  nameRegex: /^[a-zA-Z]*$/,
  firstName: /^\w+([\.-]?\w+)*@\w+([\.-]?\w+)*(\.\w{2,3})+$/,
  EmailRegex:
    /^\w+[a-zA-Z0-9.!#$%&'*+/=?^_`{|}~-]*@\w+([\.-]?\w+)*(\.\w{2,3})$/,
  PasswordRegex:
    /^(?=.*[A-Za-z])(?=.*\d)(?=.*[@$!%*#?&])[A-Za-z\d@$!%*#?&]{8,}$/,
  MobileNoRegex: /[0-9]{10}$/,
  ExpMonthYearRegex: /\d{2}\/\d{2}/,
  CardNumberRegex:
    /^(?:4[0-9]{12}(?:[0-9]{3})?|[25][1-7][0-9]{14}|6(?:011|5[0-9][0-9])[0-9]{12}|3[47][0-9]{13}|3(?:0[0-5]|[68][0-9])[0-9]{11}|(?:2131|1800|35\d{3})\d{11})$/,
  ZipCodeRegex: /(^\d{6}$)|(^\d{6}-\d{4}$)/,
  EmailAndMobileNumber:
    /^(?:\d{10}|\w+[a-zA-Z0-9.!#$%&'*+/=?^_`{|}~-]*@\w+([\.-]?\w+)*(\.\w{2,3}))$/,
  BirthdayRegex: /^((0?[1-9]|1[012])[- /.](0?[1-9]|[12][0-9]|3[01]))*$/,
};
